							<h2>Как заказать такси</h2>
							<p>Заказывать такси можно не только в программе, но и через сайт. Для этого необходимо зайти на него, ввести данные: адрес и время прибытия, место назначения, мобильный телефон. Также выбрать тариф (комфорт или эконом), по необходимости оставить комментарий, например, номер подъезда, чтобы водителю было легче сориентироваться. После введения информации нажать «Вызвать такси». Если все верно, на телефон придет смс-уведомление с номером и маркой автомобиля, контактами водителя.</p>
