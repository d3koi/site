							<h2>Взаимодействие с водителями и пассажирами</h2>
							<p>В рамках пользования услугами такси зачастую случаются конфликтные ситуации. Обусловлены они рядом разнообразных факторов. К примеру, дорога является одним из источников стресса и зачастую водители такси в таких условиях допускают появление неблагоприятных ситуаций.</p>

<p> Отдельную группу претензий составляют вопросы связанные с ущербом в результате ДТП с участием «Яндекс. Такси», которые широко освещаются в СМИ. Юридические службы компании по искам о компенсациях пострадавшим занимают позицию, что «Яндекс. Такси» всего лишь информационный сервис.</p>

<p> Сервис «Яндекс. Такси» по одному письму с указанием номера выдал полиции данные о поездках пользователя, которым оказался журналист Иван Голунов. Дело вскрыло проблемы защищённости данных о поездках пассажиров.

<p> Большое количество водителей такси воспользовалось вступившим в 2019 году в силу на территории РФ законом о самозанятных.</p>

<p> 1 марта 2020 года в приложении «Яндекс. Такси» появился рейтинг пассажиров. 7 августа 2020 года «Яндекс. Такси» начал тестировать показ рейтинга пассажиров водителям.</p>

<p> В сентябре 2020 года, «Яндекс. Такси» в сотрудничестве с банками для водителей сервиса запустила программу автокредитования. Такая услуга доступна индивидуальным предпринимателям или самозанятым, которые сотрудничают с компанией от двух месяцев, совершили за прошедший период как минимум 100 поездок и не получали блокировку от сервиса «Яндекс. Такси»</p>
